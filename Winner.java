
public class Winner {
	
	int occurrences;
	String item;
	
	public Winner(int occurrences, String item) {
		this.occurrences = occurrences;
		this.item = item;
		
		
	}

	/**
	 * @return the occurrences
	 */
	public int getOccurrences() {
		return occurrences;
	}

	/**
	 * @return the item
	 */
	public String getItem() {
		return item;
	}

}
